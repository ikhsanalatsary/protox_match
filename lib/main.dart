class _MyHomePageState extends State<MyHomePage> {
  int _counter = 0;
  String _firstName = 'John';
  String _lastName = 'Wick';
  String _message = '';

  void initState() {
    _rpc();
    super.initState();
  }

  Future<void> _rpc() async {
    final channel = ClientChannel(
      'MacBook-Pro', // change this with your local ip address or with host name
      port: 50051,
      options: const ChannelOptions(credentials: ChannelCredentials.insecure()),
    );

    final stub = HelloClient(channel);

    try {
      final request = HelloRequest();
      request..firstName = _firstName;
      request..lastName = _lastName;
      final response = await stub.greeting(request);
      print('Greeter client received: ${response.message}');
      setState(() {
        _message = response.message;
      });
    } catch (e) {
      print('Caught error: $e');
    }
    await channel.shutdown();
  }

  void _incrementCounter() {
    setState(() {
      // This call to setState tells the Flutter framework that something has
